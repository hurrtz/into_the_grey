namespace Strays.Core.Game.Data;

/// <summary>
/// The three major acts of the game's story.
/// </summary>
public enum ActState
{
    /// <summary>
    /// Act 1: Denial & Causality - Journey to NIMDOK's core.
    /// The protagonist believes the wasteland is temporary and seeks to return to the simulation.
    /// </summary>
    Act1_Denial,

    /// <summary>
    /// Act 2: Responsibility & Loss - The Boost Control System activates.
    /// The protagonist accepts reality but must deal with Bandit's deterioration.
    /// </summary>
    Act2_Responsibility,

    /// <summary>
    /// Act 3: Irreversibility - The final confrontation.
    /// The journey becomes a terminal race to contain the fallout.
    /// </summary>
    Act3_Irreversibility
}
